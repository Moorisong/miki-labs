function conjugateToModifier(word: string): string {
    if (!word) return '';
    // For Korean
    if (/[ㄱ-ㅎ|ㅏ-ㅣ|가-힣]/.test(word)) {
        if (word.endsWith('한') || word.endsWith('인') || word.endsWith('는') || word.endsWith('운') || word.endsWith('은')) {
            return word;
        }
        return word + '인';
    }
    return word;
}

export function generateDescription(
    area: 'open' | 'blind' | 'hidden' | 'unknown',
    keywords: string[],
    seedString?: string
): string {
    if (!keywords || keywords.length === 0) return '';

    const templates = TEMPLATES[area];


    // Simple string hash function for seeding
    const getSeededIndex = (arr: string[], salt: string) => {
        if (!seedString) return Math.floor(Math.random() * arr.length);

        const str = seedString + area + salt + keywords.join('');
        let hash = 0;
        for (let i = 0; i < str.length; i++) {
            const char = str.charCodeAt(i);
            hash = ((hash << 5) - hash) + char;
            hash |= 0; // Convert to 32bit integer
        }
        return Math.abs(hash) % arr.length;
    };

    const uniqueKeywords = Array.from(new Set(keywords));
    const k1 = uniqueKeywords[0];
    const k2 = uniqueKeywords.length > 1 ? uniqueKeywords[1] : (uniqueKeywords.length > 0 ? uniqueKeywords[0] : '');

    const k1Mod = conjugateToModifier(k1);
    const k2Mod = conjugateToModifier(k2);

    // 1단계: 칭찬 (Praise)
    const s1 = templates.praise[getSeededIndex(templates.praise, 'praise')].replace('${k1Mod}', k1Mod);

    // 2단계: 반전 (BUT)
    const s2 = templates.but[getSeededIndex(templates.but, 'but')].replace('${k1Mod}', k1Mod).replace('${k2Mod}', k2Mod);

    // 3단계: 찔림 (Sting)
    const s3 = templates.sting[getSeededIndex(templates.sting, 'sting')];

    // 4단계: 마무리 (Human Closing)
    const s4 = templates.closing[getSeededIndex(templates.closing, 'closing')];

    return `${s1} ${s2} ${s3} ${s4}`;
}
